#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*tmp;
	int		i;
	size_t	len;

	i = 0;
	len = ft_strlen(s1);
	if (!(tmp = (char*)malloc(sizeof(char) * len + 1)))
		return (NULL);
	while (s1[i] != '\0')
	{
		tmp[i] = s1[i];
		i++;
	}
	tmp[i] = '\0';
	return (tmp);
}
